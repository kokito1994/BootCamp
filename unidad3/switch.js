// Sentencias Switch

let nota = 5;
switch (nota) {
    case  5:
        console.log("bueno trabajo, sobresaliente");
        break;
    case  4:
        console.log("buen trabajo");
        break;
    case 3:
        console.log("has aprobado")
        break;
    case 2:
        console.log("no aprobaste por poco");
        break;
    case 1:
        console.log("no has estudiado esfuerzate");
        break;
    default:
        console.log("Error");
        break;
}